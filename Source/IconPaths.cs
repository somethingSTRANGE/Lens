      // ── Path builder ───────────────────────────────────────────────────────────────────

      /// <summary>
      ///    Parses an SVG path <c>d</c> string and returns a
      ///    <see cref="GraphicsPath" /> scaled so the 640-unit viewBox fits within
      ///    <paramref name="size" /> pixels. Supports absolute M, L, C, Z only.
      ///    Uses <see cref="FillMode.Winding" /> to match SVG's nonzero fill rule,
      ///    so inner counterclockwise sub-paths correctly punch holes.
      /// </summary>
      public static GraphicsPath Build(string[] segments, float size)
      {
         var scale = size / ViewSize;
         var path = new GraphicsPath(FillMode.Winding);

         float cx = 0, cy = 0;
         float mx = 0, my = 0;

         foreach (var seg in segments)
         {
            var cmd = seg[0];
            var args = seg.Length > 1 ? seg.Substring(1).Trim() : "";
            var nums = args.Length == 0
               ? Array.Empty<float>()
               : Array.ConvertAll(args.Split(' '),
                  s => float.Parse(s, CultureInfo.InvariantCulture) * scale);

            switch (cmd)
            {
               case 'M':
                  cx = mx = nums[0];
                  cy = my = nums[1];
                  path.StartFigure();
                  break;

               case 'L':
                  path.AddLine(cx, cy, nums[0], nums[1]);
                  cx = nums[0];
                  cy = nums[1];
                  break;

               case 'C':
                  path.AddBezier(cx, cy,
                     nums[0], nums[1],
                     nums[2], nums[3],
                     nums[4], nums[5]);
                  cx = nums[4];
                  cy = nums[5];
                  break;

               case 'Z':
               case 'z':
                  path.CloseFigure();
                  cx = mx;
                  cy = my;
                  break;
            }
         }

         return path;
      }
